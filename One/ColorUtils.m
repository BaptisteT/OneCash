#import "ColorUtils.h"

@implementation ColorUtils

+ (UIColor *)veryLightGreen {
    return [UIColor colorWithRed:239./255. green:255./255. blue:243./255 alpha:1];
}

+ (UIColor *)lightGreen {
    return [UIColor colorWithRed:206./255. green:246./255. blue:216./255 alpha:1];
}

+ (UIColor *)mainGreen {
    return [UIColor colorWithRed:55./255. green:229./255. blue:96./255 alpha:1];
}

+ (UIColor *)darkGreen {
    return [UIColor colorWithRed:54./255. green:204./255. blue:90./255 alpha:1];
}


+ (UIColor *)red {
    return [UIColor colorWithRed:255./255. green:68./255. blue:68./255 alpha:1];
}

@end
